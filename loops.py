i = 1
# while loop only executes when the condition is true
while(i<=4):
    print(i)
    i+=1 # i=i+1
# there is also a for loop and we can also use
# else statement in for loop which will only be executed on the completion of the for loop
l=[1,2,3,4]
for item in l:
    print(item)

else:
    print("done")# will be printed once we are out of the loop
 


